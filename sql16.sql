SHOW DATABASES;
USE lab;

-- PRIMARY KEY
CREATE TABLE ST(
ID INT PRIMARY KEY,
NAME VARCHAR(20),
AGE INT
);

-- UNIQUE KEY
CREATE TABLE UQ(
ISBN INT UNIQUE KEY,
NAME VARCHAR(250),
AUTHOR VARCHAR(200)
);

-- NOT NULL KEY
CREATE TABLE NT(
ID INT NOT NULL,
NAME VARCHAR(20) NOT NULL,
AGE INT NOT NULL
);

-- FOREIGN KEY
CREATE TABLE CUSTOMER(
CUST_ID INT PRIMARY KEY,
NAME VARCHAR(20) NOT NULL
);

CREATE TABLE ORDERS(
ORDER_ID INT PRIMARY KEY,
CUST_ID INT,
ORDER_DATE DATE,
FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID)
);

